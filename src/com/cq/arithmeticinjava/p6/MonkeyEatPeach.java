public class MonkeyEatPeach {
    
    public static long peachNum(int n){
        if(n==1){
           return   1L;
        }
        else{
            return (peachNum(n-1)+1)*2;
        }
    }
    public static void main(String[] args) {
        System.out.println(peachNum(10));
    }
}
